const myAge = 23;

let earlyYears = 2;
earlyYears = earlyYears * 12;

let laterYears = myAge - 2;
laterYears = laterYears * 4;

let myAgeInCatYears = earlyYears + laterYears;

const myName = 'Даниил'.toUpperCase();

console.log(`Меня зовут ${myName} мне ${myAge} года, а в кошачих годах мне ${myAgeInCatYears} лет.`);